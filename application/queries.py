# Lekérdező függvények

# Kilistázni havi bontásban azon települések nevét és közterületeket, ahol ebben a hónapban a legtöbb rablást jelentették.


# Kilistázni azoknak a diszpécsereknek az adatait havi bontásban (év, hónap), akik egy-egy hónapban a legtöbb hívást fogadták.


# Táblázatos formában kilistázni azon telefonálók nevét és telefonszámát, akik egy hónapon belül többször telefonáltak ugyanolyan típusú eset miatt.


# Kilistázni azokat a hívásokat, amelyekhez az intézkedés csak 10 perccel később történt meg.
